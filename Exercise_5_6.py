#Life stages
age = 25

if age < 2:
    print("You are a baby!")

elif age >= 2 and age < 4:
    print("You are toddler.")

elif age >= 4 and age < 13:
    print("You are kid.")

elif age >=13 and age < 20:
    print("You are teenager.")

elif age >=20 and age < 65:
    print("You are adult.")

else :
    print("You are older.")    

